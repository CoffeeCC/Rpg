export function speciesById(id: string): SpeciesDef | undefined {
  return SPECIES[id];
}

export function speciesMatching(families: MonsterFamily[], tierMin: number, tierMax: number): SpeciesDef[] {
  return Object.values(SPECIES).filter(
    (s) => families.includes(s.family) && s.tier >= tierMin && s.tier <= tierMax
  );
}
